// ─────────────────────────────────────────
// Novels Note JP — #タグ 除去ロジック（共通化）
//
// Export（exporter.ts）・小説閲覧ビュー（novelReadingView.ts）・
// 文字数カウント（wordCount.ts）の3箇所で同じタグ判定ロジックが
// 必要になるため、ここに集約する。
// 「どこからどこまでが #タグ か」の判定基準を1箇所にまとめることで、
// 片方だけ修正してExport結果と文字数カウントの挙動が食い違う事故を防ぐ。
//
// タグの定義：「#」+「スペース（半角・全角）以外の1文字以上」
// タグの終端：スペース（半角・全角）または行末
// 「#」の直前が行頭または空白でない場合はタグ開始とみなさない。
// これにより「C#は好き」のような文中の「#」を誤ってタグとして
// 除去し、後続の文章を消してしまう事故を防ぐ。
//
//   例）「この文章 #確認 は後で見直す」→「この文章  は後で見直す」
//       「#メモ この行は仮」→「この行は仮」
//       「C#は好き」→「C#は好き」（変更なし）
//
// タグ除去後に残る連続スペース・行頭末尾スペースの正規化は、
// 呼び出し元（Export／小説閲覧ビュー／文字数カウント）ごとに
// 既存の処理に委ねる。本関数はタグ判定のみを担当する。
// ─────────────────────────────────────────
export function stripHashtags(text: string): string {
  // ① 行全体がタグだけの行 → 行ごと削除
  text = text.replace(/^[ \t\u3000]*#\S+[ \t\u3000]*$/gm, "");

  // ② タグ本体＋直後スペースを除去（行頭・行中どちらでも対応）
  //    連続タグも1パスですべて除去できる。
  text = text.replace(/(?<=^|\s)#\S+[ \t\u3000]?/gm, "");

  return text;
}
